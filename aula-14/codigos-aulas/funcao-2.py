def produto(x, y):
    resultado = x * y
    return resultado

multiplicacao1 = produto(4, 8)
multiplicacao2 = produto(3, 9)
multiplicacao3 = produto(10, 5)

print(f"Resultado de 4 vezes 8: {multiplicacao1}")
print(f"Resultado de 3 vezes 9: {multiplicacao2}")
print(f"Resultado de 10 vezes 5: {multiplicacao3}")